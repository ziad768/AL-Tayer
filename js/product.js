function cartAdd(id,category,addOrLoss){
    var cartItems = JSON.parse(localStorage.getItem("cart"))
    var indexItem = cartItems.findIndex((ele) => ele.id == id && ele.category == category);
    var price = +localStorage.price
    if(addOrLoss == "add"){
        cartItems[indexItem].numberCart++
        price += cartItems[indexItem].price  
    }else{
        cartItems[indexItem].numberCart--
        price -= cartItems[indexItem].price  
    }
    localStorage.setItem( "cart" , JSON.stringify(cartItems) )
    localStorage.setItem("price" , price)
    artCart()
}
