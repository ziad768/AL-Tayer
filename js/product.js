function favorite(category,id){
    var item = products[category].find((ele) => ele.id == id)
    var items = localStorage.getItem("favorite") != null ? JSON.parse(localStorage.getItem("favorite")) : [];
    var buttonFav =document.querySelector(`.fav${id}`)
    if (buttonFav.innerHTML == `<i class="far fa-heart"></i>`) {
        buttonFav.innerHTML = `<i class="fas fa-heart text-danger"></i>`
        items = [...items,item]
    }else{
        buttonFav.innerHTML = `<i class="far fa-heart"></i>`
        var x = items.findIndex((ele) => ele.id == item.id )
        items.splice(x ,1)
    }
    localStorage.setItem("favorite" , JSON.stringify(items))
}
